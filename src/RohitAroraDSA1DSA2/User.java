package BatchMarch;

public class User {

    String email;
    int phoneNo;
    int whatsappNumber;
    String name;

    public User(String email, int phoneNo, int whatsappNumber, String name) {
        this.email = email;
        this.phoneNo = phoneNo;
        this.whatsappNumber = whatsappNumber;
        this.name = name;
    }
}
